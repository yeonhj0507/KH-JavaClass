public class day1 {

	public static void main(String[] args) {
		int num1 = 12;
		int num2 = 10;
		int result = num1 + num2; //메모리에 저장된 num1 , num2 값으로 계산하여 새로운 메모리에 저장
		
		System.out.println(num1 + " + " + num2 + " = " + result); //숫자열 출력
		System.out.printf("%d + %d = %d", num1, num2, result);   //문자열 출력
		
	}

}
